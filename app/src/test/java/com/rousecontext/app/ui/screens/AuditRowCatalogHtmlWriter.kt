/**
 * Metadata for a single audit-row variant captured during
 * [AuditRowVariantsScreenshotTest]. Each variant produces one light + one
 * dark PNG; [screenshotBase] names the pair (e.g. `01_third_row`).
 *
 * Image paths in the rendered HTML are RELATIVE to the HTML file's location
 * (`docs/internal/audit-row-variants/index.html`), so the Pages workflow
 * copies the screenshots into a sibling `screenshots/` directory.
 */
internal data class CapturedVariant(
    val screenshotBase: String,
    val label: String,
    val description: String
) {
    val lightPng: String get() = "screenshots/${screenshotBase}_light.png"
    val darkPng: String get() = "screenshots/${screenshotBase}_dark.png"
}

/**
 * Thread-safe collector for [CapturedVariant]s. Populated by the screenshot
 * test's light + dark captures; the first record for a given base name wins
 * so the duplicate dark call is a no-op.
 */
internal object CapturedVariants {
    private val byBase: MutableMap<String, CapturedVariant> =
        Collections.synchronizedMap(LinkedHashMap())

    fun record(base: String, label: String, description: String) {
        synchronized(byBase) {
            if (byBase.containsKey(base)) return
            byBase[base] = CapturedVariant(
                screenshotBase = base,
                label = label,
                description = description
            )
        }
    }

    fun snapshot(): List<CapturedVariant> = synchronized(byBase) {
        byBase.values.toList()
    }

    fun clear() = synchronized(byBase) { byBase.clear() }
}

/**
 * Writes the audit-row variants catalog HTML to [outputFile]. Called from
 * the screenshot test's `@ClassRule` once every variant has been captured,
 * so the HTML references only screenshots that actually landed on disk.
