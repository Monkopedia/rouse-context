    fun write(outputFile: File, variants: List<CapturedVariant>) {
        outputFile.parentFile?.mkdirs()
        outputFile.writeText(render(variants))
    }

    fun render(variants: List<CapturedVariant>): String {
        val sorted = variants.sortedBy { it.screenshotBase }
        return buildString {
            append(HEADER)
            append("<div class=\"container\">\n")
            append(
                "<p class=\"generated-note\">Generated by " +
                    "<code>AuditRowVariantsScreenshotTest</code>. Seven candidate " +
                    "client-label placement options for <code>ToolCallRow</code> " +
                    "(see <a href=\"" +
                    "https://github.com/monkopedia/rouse-context/issues/343" +
                    "\">#343</a>). " +
                    "Each variant renders three sample client labels &mdash; short " +
                    "(<code>Claude</code>), long (<code>Claude Desktop</code>), and " +
                    "unknown (<code>Unknown (#1)</code>) &mdash; in both light and " +
                    "dark theme. Production <code>ToolCallRow</code> is not " +
                    "modified.</p>\n"
            )
            for (variant in sorted) {
                append(renderVariant(variant))
            }
            append("</div>\n")
            append(FOOTER)
        }
    }

    private fun renderVariant(v: CapturedVariant): String = buildString {
        append("  <article class=\"variant\">\n")
        append("    <h2>").append(escape(v.label)).append("</h2>\n")
        append("    <p class=\"description\">").append(escape(v.description)).append("</p>\n")
        append("    <div class=\"screenshot-pair\">\n")
        append("      <figure>\n")
        append("        <img src=\"").append(escape(v.lightPng)).append("\" alt=\"")
            .append(escape(v.label)).append(" light\">\n")
        append("        <figcaption>Light</figcaption>\n")
        append("      </figure>\n")
        append("      <figure>\n")
        append("        <img src=\"").append(escape(v.darkPng)).append("\" alt=\"")
            .append(escape(v.label)).append(" dark\">\n")
        append("        <figcaption>Dark</figcaption>\n")
        append("      </figure>\n")
        append("    </div>\n")
        append("  </article>\n")
    }

    private fun escape(s: String): String = buildString(s.length) {
        for (ch in s) {
            when (ch) {
                '&' -> append("&amp;")
                '<' -> append("&lt;")
                '>' -> append("&gt;")
                '"' -> append("&quot;")
                '\'' -> append("&#39;")
                else -> append(ch)
            }
        }
    }

